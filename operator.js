let b = 2
let a = 5
console.log("Oberators in JavaScript");
console.log("a+b =", a + b); // Addition
console.log("b-a =", b - a); // Subtraction
console.log("a*b =", a * b); // Multiplication
console.log("a/b =", a / b); // Division
console.log("a%b =", a % b); // Modulus
console.log("a**b =", a ** b); // Exponentiation


// increment and decrement operators ----------------

a++;
console.log("After increment, a =", a); // Increment +1
b-=2;
console.log("After decrement, b =", b); // Decrement -2

// comparison operators ----------------

a = 10;
b = 20;

console.log("a == b:", a == b); // Equal to
console.log("a === b:", a === b); // Strict equal to(type and value)
console.log("a != b:", a != b); // Not equal to
console.log("a !== b:", a !== b); // Strict equal to(type and value)
console.log("a > b:", a > b);   // Greater than
console.log("a < b:", a < b);   // Less than
console.log("a >= b:", a >= b); // Greater than or equal to
console.log("a <= b:", a <= b); // Less than or equal to


// shorthand way to write simple if...else.

let agee = 17;
let result = agee >= 18 ? "Adult" : "Minor"; // Ternary Operator if(Adult) else(Minor)
console.log(result) // "Minor"









